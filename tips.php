	<h2>SEASONAL MAINTENANCE</h2>  
	<div class="panel panel-default">
      <div class="panel-heading">
        <h4 class="panel-title">
          <a data-toggle="collapse" data-parent="#accordion" href="#collapse3"><ul class="fa-ul">
	<li><i class="fa-li fa fa-check-square"></i>CLEAN BRAKE DUST OFF REGULARLY</ul></a>
        </h4>
      </div>
      <div id="collapse3" class="panel-collapse collapse in">
        <div class="panel-body">
		<p>Brake dust contains all sorts of nasty stuff. If you leave it too long, the combination of road grime, moisture and heat from your brakes will bake it on to your wheels. Brake dust normally clings to wheels with static electricity so a damp sponge and clean cold water is the best way to get it off.</p>
		</div>
      </div>
    </div>
    <div class="panel panel-default">
      <div class="panel-heading">
        <h4 class="panel-title">
          <a data-toggle="collapse" data-parent="#accordion" href="#collapse4"><ul class="fa-ul">
	<li><i class="fa-li fa fa-check-square"></i>CHECK YOUR BELTS</ul></a>
        </h4>
      </div>
      <div id="collapse4" class="panel-collapse collapse">
        <div class="panel-body">
			<p>At the front of your engine there will be a series of rubber drive belts that loop around various pulleys, driving everything from the alternator to the a/c compressor. Rubber perishes, more so in extreme conditions like those found in an operating engine bay. Get your timing belt and accessory drive belt checked every 25,000 miles, preferably replacing it every 50,000 miles.</p>
		</div>
      </div>
    </div>
    <div class="panel panel-default">
      <div class="panel-heading">
        <h4 class="panel-title">
          <a data-toggle="collapse" data-parent="#accordion" href="#collapse5"><ul class="fa-ul">
	<li><i class="fa-li fa fa-check-square"></i>CHECKING YOUR OIL LEVEL</ul></a>
        </h4>
      </div>
      <div id="collapse5" class="panel-collapse collapse">
        <div class="panel-body">
			<p>This is something everyone can do - it's quick and easy and it'll tell you if your engine needs oil. If the oil is too high or too low, it can cause trouble for your engine. To check the oil, park on level ground and wait until the engine has cooled down after driving, then locate the dipstick. Pull it out and wipe it clean, then push it all the way back in until the top of it is seated properly in the dip tube again. Wait a moment then pull it out again. Check the level of the oil. If it's between the high and low marks, you're fine. (If it's too low, add a little.) The high and low marks can be denoted by two dots, an "H" and "L" or a shaded area on the dipstick.</p>
			</div>
      </div>
    </div>
	<div class="panel panel-default">
      <div class="panel-heading">
        <h4 class="panel-title">
          <a data-toggle="collapse" data-parent="#accordion" href="#collapse6"><ul class="fa-ul">
	<li><i class="fa-li fa fa-check-square"></i>CHECKING YOUR COOLANT LEVEL</ul></a>
        </h4>
      </div>
      <div id="collapse6" class="panel-collapse collapse">
        <div class="panel-body">
	<p>Again, something everyone can do. The coolant is the other thing your engine cannot go without. Every engine is different but if you check your handbook you should find where the coolant reservoir is. It will normally be bolted to one side of the engine bay or the other, and be a white semi-transparent bottle. Wait until your engine is cool and take a look at it - the outside should have 'low' and 'high' markings on it and the level of coolant inside should be between the two.</p>
		<p><strong>CAUTION:</strong>Do not take the radiator cap off to check coolant levels. If the coolant system is still hot then it is still under pressure and the pressure release will burn you.</p>

			</div>
      </div>
    </div>
	<div class="panel panel-default">
      <div class="panel-heading">
        <h4 class="panel-title">
          <a data-toggle="collapse" data-parent="#accordion" href="#collapse7"><ul class="fa-ul">
	<li><i class="fa-li fa fa-check-square"></i>KEEPING YOUR PAINT FINISH IN GOOD CONDITION</ul></a>
        </h4>
      </div>
      <div id="collapse7" class="panel-collapse collapse">
        <div class="panel-body">
	<p>Only choose superior car detailing supplies to keep your car's interior and exterior like new. There are plenty of decent quality waxes and polishes on the market today and if the last time you waxed or polished your car was over a decade ago, things have changed considerably since then. The wax and polish compounds are far more sophisticated both in terms of protection from fading, the look of the finish, and the ease of application. For example Turtle Wax Ice gets rid of that old problem of white wax residue.</p>
	<p><strong>TIP:</strong>If you find a bird has pooped on your car, wash it off as quick as you can. There are compounds in bird waste that can damage most car paint jobs. If you leave it in the sun and the poop bakes on, you could end up with a dull spot in the paint.</p>
		</div>
      </div>
    </div>	
	<div class="panel panel-default">
      <div class="panel-heading">
        <h4 class="panel-title">
          <a data-toggle="collapse" data-parent="#accordion" href="#collapse8"><ul class="fa-ul">
	<li><i class="fa-li fa fa-check-square"></i>DISCONNECTING AND RECONNECTING YOUR BATTERY</ul></a>
        </h4>
      </div>
      <div id="collapse8" class="panel-collapse collapse">
        <div class="panel-body">
	<p>If you're going to do any work on your car involving the electrical system, disconnect the battery first. To do this, loosen the connector for the negative/ground terminal first, and wiggle the terminal cap off. Use a wire-tie or similar to tie the cable back out of the way. If you need to take the battery out, you can now take off the positive connector.</p>
	<p>Why negative then positive? If you disconnect the positive side of the battery first, the negative side is still connected to the entire car. If you drop a tool and it lands on the positive battery terminal and touches anything else on the car, you'll have an electrical short. By disconnecting the negative first, you're cutting off the return path for the current. Now, if a tool drops on to either of the battery terminals, it doesn't matter if it touches part of the chassis or not - there's no continuous path for the electrical current.</p>
	<p>Reconnecting your battery. Connect the positive terminal first, and the negative second - the reverse of removal, and for the same reasons. When you slip the negative connector on, there will be a spark as it gets close and makes contact with the negative battery terminal. Don't be afraid of this - it's nothing to worry about. Make sure the terminal caps are done up nice and tight.</p>
	</div>
      </div>
    </div>
	<div class="panel panel-default">
      <div class="panel-heading">
        <h4 class="panel-title">
          <a data-toggle="collapse" data-parent="#accordion" href="#collapse9"><ul class="fa-ul">
	<li><i class="fa-li fa fa-check-square"></i>CHECK YOUR BATTERY TERMINALS</ul></a>
        </h4>
      </div>
      <div id="collapse9" class="panel-collapse collapse">
        <div class="panel-body">
		<p>Most modern cars run on a 12 volt negative ground electrical system. If your battery terminals or contacts aren't clean, you're making it more difficult for the current to pass around the electrical system. Remove the terminal caps as described above and clean each contact post with a wire brush to get a nice clean metal contact surface. Do the same to the terminal caps, then reattach them as described above.</p>
	</div>
      </div>
    </div>
	<div class="panel panel-default">
      <div class="panel-heading">
        <h4 class="panel-title">
          <a data-toggle="collapse" data-parent="#accordion" href="#collapse10"><ul class="fa-ul">
	<li><i class="fa-li fa fa-check-square"></i>ONE INDICATOR OR BLINKER IS FLASHING FASTER THAN THE OTHER</ul></a>
        </h4>
      </div>
      <div id="collapse10" class="panel-collapse collapse">
        <div class="panel-body">
		<p>When you indicate one way and the blinker flashes quicker than when you indicate the other way, it means one of the bulbs has blown. An auto parts store will be able to tell you what sort of bulb you need to replace it with and your manual should show you how to get at the indicator bulbs - they're different on every car.</p>
		</div>
      </div>
    </div>
	<div class="panel panel-default">
      <div class="panel-heading">
        <h4 class="panel-title">
          <a data-toggle="collapse" data-parent="#accordion" href="#collapse11"><ul class="fa-ul">
	<li><i class="fa-li fa fa-check-square"></i>DON'T TOUCH THE GLASS WHEN CHANGING HEADLIGHT BULBS</ul></a>
        </h4>
      </div>
      <div id="collapse11" class="panel-collapse collapse">
        <div class="panel-body">
	<p>Most headlight bulbs now are filled with halogen and have special coatings on the outside of the glass. If you pick the bulb up by the glass with your fingers, you will leave trace amounts of oil and grease on the glass. When the bulb is used, that area of the glass will get hotter than the rest and it will eventually cause the bulb to crack. When changing headlight bulbs, only hold the metal bulb holder at the base, or make sure you're wearing rubber surgical / mechanic's gloves (clean ones) if you're touching the glass.</p>
		</div>
      </div>
    </div>
	<div class="panel panel-default">
      <div class="panel-heading">
        <h4 class="panel-title">
          <a data-toggle="collapse" data-parent="#accordion" href="#collapse12"><ul class="fa-ul">
	<li><i class="fa-li fa fa-check-square"></i>FUEL FILTER</ul></a>
        </h4>
      </div>
      <div id="collapse12" class="panel-collapse collapse">
        <div class="panel-body">
<p>This is an item that’s probably as important as it is overlooked. Assuming yours is a fuel-injected car, the fuel filter is more or less on continuous duty whenever your ignition is on and the fuel pump is active, preventing impurities from clogging injectors. So, over time, replacement will be necessary. Locations vary from beneath the hood or undercarriage to inside the fuel tank itself. Since systems are often pressurized and fuel lines need to be safely crimped, you might prefer to let a technician handle it.</p>
	
	<p><strong>When to replace:</strong> Every 2 years or 24,000 miles.</p>		</div>
